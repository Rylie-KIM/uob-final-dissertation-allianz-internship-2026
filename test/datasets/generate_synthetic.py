"""
Dataset Generator — Synthetic Insurance Claims
================================================
Generates and saves the synthetic datasets used by all tests and builds.

Run this ONCE before running tests or builds for the first time:
    python datasets/generate_synthetic.py

Output files (all in datasets/synthetic/):
    claims_small.csv        — 3,000 claims   (fast unit tests)
    claims_medium.csv       — 10,000 claims  (integration tests, evaluation)
    claims_large.csv        — 50,000 claims  (full simulation, policy comparison)
    claims_with_versions.csv — 10,000 claims with 3 model version scores +
                               investigation flags + observed/true fraud labels
    loop_metrics.csv        — per-version loop metrics from the full simulation
    README.txt              — column dictionary
"""

import sys
import os

# Add builds directories to path
_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
for _sub in os.listdir(os.path.join(_ROOT, "builds")):
    _path = os.path.join(_ROOT, "builds", _sub)
    if os.path.isdir(_path):
        sys.path.insert(0, _path)

import numpy as np
import pandas as pd
from pathlib import Path

from simulate_sfp_loop import generate_claims, run_full_simulation

OUT_DIR = Path(__file__).parent / "synthetic"
OUT_DIR.mkdir(exist_ok=True)


# ─────────────────────────────────────────────
# 1. Plain claims (no model scores yet)
# ─────────────────────────────────────────────

def make_plain_claims(n: int, name: str, seed: int = 42):
    print(f"  Generating {name} ({n:,} claims)...", end=" ")
    df = generate_claims(n_claims=n, seed=seed)
    path = OUT_DIR / f"{name}.csv"
    df.to_csv(path, index=False)
    print(f"→ {path.name}  [{df.shape[0]:,} rows × {df.shape[1]} cols]")
    return df


# ─────────────────────────────────────────────
# 2. Claims with model versions + loop
# ─────────────────────────────────────────────

def make_versioned_claims(n: int, n_versions: int = 3, epsilon: float = 0.0, seed: int = 42):
    label = f"claims_with_versions{'_eps' if epsilon > 0 else ''}"
    print(f"  Simulating SFP loop ({n:,} claims, {n_versions} versions, ε={epsilon})...", end=" ")
    df, metrics = run_full_simulation(
        n_claims=n, n_versions=n_versions,
        investigation_rate=0.25, epsilon=epsilon, seed=seed
    )

    # Use v{n_versions} as the "current" model
    last = n_versions
    df["investigated"]   = df[f"model_v{last}_investigated"].astype(int)
    df["observed_fraud"] = pd.to_numeric(df[f"model_v{last}_observed_fraud"], errors="coerce")

    path = OUT_DIR / f"{label}.csv"
    df.to_csv(path, index=False)
    print(f"→ {path.name}  [{df.shape[0]:,} rows × {df.shape[1]} cols]")

    # Save loop metrics
    metrics_df = pd.DataFrame(metrics)
    mpath = OUT_DIR / f"loop_metrics{'_eps' if epsilon > 0 else ''}.csv"
    metrics_df.to_csv(mpath, index=False)
    print(f"     Metrics → {mpath.name}")

    return df, metrics_df


# ─────────────────────────────────────────────
# 3. README / column dictionary
# ─────────────────────────────────────────────

COLUMN_DICT = """
SYNTHETIC INSURANCE CLAIMS — COLUMN DICTIONARY
===============================================
Generated by: datasets/generate_synthetic.py
DGP: logit(P(fraud)) = -2.5 + 0.8*high_amount + 0.6*night_claim
                             + 1.2*high_postcode + 0.4*prior_claims + N(0, 0.3)

───────── BASE CLAIM FEATURES ─────────────────────────────────────────────────
claim_id          str     Unique claim identifier (CLM000001 format)
claim_amount      float   Raw claim amount in GBP (log-normal, mean ~exp(7.5))
claim_hour        int     Hour of claim submission (0–23)
postcode_risk     float   Postcode-level fraud risk index (0=low, 1=high)
prior_claims      int     Number of prior claims by same claimant (0–5)
claim_type        str     Claim type: theft | damage | injury

───────── DERIVED BINARY FEATURES (used as model inputs) ──────────────────────
high_amount       int     1 if claim_amount > 75th percentile, else 0
night_claim       int     1 if claim_hour in {22,23,0,1,2,3,4,5}, else 0
high_postcode     int     1 if postcode_risk > 0.6, else 0

───────── GROUND TRUTH (oracle — unknown in production) ───────────────────────
true_fraud        int     1 if claim is genuinely fraudulent, else 0
true_fraud_prob   float   True underlying fraud probability

───────── MODEL VERSION SCORES (versions 1, 2, 3) ─────────────────────────────
model_v{N}_score        float  Predicted fraud probability from model version N
model_v{N}_investigated int    1 if investigated under model vN's policy, else 0
model_v{N}_observed_fraud float 1/0 if investigated and fraud found/not found
                                NaN if uninvestigated (label is MISSING)

───────── CONVENIENCE COLUMNS (added in claims_with_versions.csv) ─────────────
investigated      int     = model_v3_investigated (current model's decision)
observed_fraud    float   = model_v3_observed_fraud (NaN for uninvestigated)

───────── LOOP METRICS (loop_metrics.csv) ──────────────────────────────────────
version                     int    Model version number (1, 2, 3, ...)
n_investigated              int    Number of claims investigated in that generation
investigation_rate          float  Fraction of all claims investigated
observed_fraud_rate         float  Fraud rate WITHIN investigated claims (BIASED)
auc_on_investigated         float  AUC computed on investigated test set (BIASED)
true_recall                 float  Recall vs ground truth (ALL claims)
precision                   float  Precision on investigated claims
loop_amplification_factor   float  fraud_rate_top_q / true_fraud_rate (>1 = loop active)
blind_spot_fraud_fraction   float  Fraction of true fraud in NEVER-investigated claims

KEY RELATIONSHIPS:
  observed_fraud_rate >> true_fraud_rate    ← investigation bias inflates observed rate
  auc_on_investigated >> true_auc           ← biased evaluation looks better than reality
  true_recall decreases over versions       ← loop creates growing blind spots
  loop_amplification_factor increases       ← model gets better at finding where it looks,
                                              worse at finding fraud it doesn't look for
"""


# ─────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────

if __name__ == "__main__":
    print("=" * 60)
    print("Generating synthetic insurance claims datasets")
    print(f"Output directory: {OUT_DIR}")
    print("=" * 60)

    # 1. Plain claims (no loop)
    print("\n[1/5] Plain claims datasets")
    make_plain_claims(3_000,  "claims_small",  seed=42)
    make_plain_claims(10_000, "claims_medium", seed=42)
    make_plain_claims(50_000, "claims_large",  seed=42)

    # 2. Versioned claims (loop active)
    print("\n[2/5] Loop-active simulation (ε=0)")
    make_versioned_claims(n=10_000, n_versions=3, epsilon=0.0, seed=42)

    # 3. Versioned claims (exploration enabled)
    print("\n[3/5] Exploration simulation (ε=0.10)")
    make_versioned_claims(n=10_000, n_versions=3, epsilon=0.10, seed=42)

    # 4. Write README
    print("\n[4/5] Writing column dictionary...")
    (OUT_DIR / "README.txt").write_text(COLUMN_DICT)
    print(f"  → README.txt")

    # 5. Quick sanity check
    print("\n[5/5] Sanity check...")
    df_check = pd.read_csv(OUT_DIR / "claims_with_versions.csv")
    invest_rate   = df_check["investigated"].mean()
    true_fr       = df_check["true_fraud"].mean()
    obs_fr        = df_check.loc[df_check["investigated"] == 1, "observed_fraud"].mean()
    coverage      = df_check["investigated"].mean()
    nan_uninvest  = df_check.loc[df_check["investigated"] == 0, "observed_fraud"].isna().all()

    print(f"  Claims: {len(df_check):,}")
    print(f"  True fraud rate          : {true_fr:.3f}  ← ground truth")
    print(f"  Observed fraud rate      : {obs_fr:.3f}  ← biased (investigated only)")
    print(f"  Investigation coverage   : {coverage:.1%}")
    print(f"  Uninvestigated = NaN     : {nan_uninvest}  ← must be True")
    assert nan_uninvest, "Uninvestigated claims should have NaN observed_fraud!"
    assert obs_fr > true_fr, "Observed fraud rate should exceed true rate (SFP effect)!"

    print("\n✅ All datasets generated and verified.")
    print("\nFiles created:")
    for f in sorted(OUT_DIR.iterdir()):
        size_kb = f.stat().st_size / 1024
        print(f"  {f.name:<45} {size_kb:>8.1f} KB")
